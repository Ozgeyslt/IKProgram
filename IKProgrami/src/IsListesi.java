public class IsListesi {
    private String is;
    private String isEkle;
    private String isTamamla;

    public IsListesi(String is, String isEkle, String isTamamla) {
        this.is = is;
        this.isEkle = isEkle;
        this.isTamamla = isTamamla;
    }

    public String getIs() {
        return is;
    }

    public void setIs(String is) {
        this.is = is;
    }

    public String getIsEkle() {
        return isEkle;
    }

    public void setIsEkle(String isEkle) {
        this.isEkle = isEkle;
    }

    public String getIsTamamla() {
        return isTamamla;
    }

    public void setIsTamamla(String isTamamla) {
        this.isTamamla = isTamamla;
    }
}
